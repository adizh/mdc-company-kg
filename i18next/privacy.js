import privacyRu from "../locales/ru/privacyRu.js";


const selectedLanguage = localStorage.getItem("selectedLanguage");
i18next.init({
  lng:  selectedLanguage && selectedLanguage != null && selectedLanguage.length > 0
  ? selectedLanguage
  : "ru", // if you're using a language detector, do not define the lng option
  debug: true,
  resources: {
  
    ru: {
      translation: privacyRu,
    },
  
  },
});
updateContent();

export function changeLng(lng) {
  i18next.changeLanguage(lng);
  localStorage.setItem("selectedLanguage", lng);
}

i18next.on("languageChanged", () => {
  updateContent();
});
